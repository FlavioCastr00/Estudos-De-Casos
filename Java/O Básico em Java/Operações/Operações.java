public class Operações
{
    public static void main(String[] args)
    {
        int a = -5 + 8 * 6;
        int b = (55 + 9) % 9;
        int c = 20 + -3 * 5 / 8;
        int d = 5 + 15 / 3 * 2 - 8 % 3;
        
        System.out.println("Resultado da operação A = " + a +
                           "\nResultado da operação B = " + b +
                           "\nResultado da operação C = " + c +
                           "\nResultado da operação D = " + d);
    }
}